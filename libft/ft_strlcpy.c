#include "libft.h"

int	ft_strlcpy(char *dst, const char *src, int dstsize)
{
	int		i;
	int		sorc;

	sorc = ft_strlen(src);
	i = 0;
	if (dstsize == 0)
		return (sorc);
	while (src[i] && (i < dstsize - 1))
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (sorc);
}
